let i = 0;
/*Виводить парні числа*/
while (i <= 100) {
    i++;
    if (i%2===0) {
        console.log ('Парні числа:',i);
}
}
/*Виводить числа, які при діленні на 3 не мають залишку*/
while (i <= 100) {
    i++;
    if (i%3===0) {
        console.log ('Числа, які при діленні на 3 не мають залишку:',i);
}
}